package com.backend.EMS.Model;

/**
 * Enumeration representing various user roles within the system.
 */
public enum Role {
    /**
     * Represents the role of an Admin user.
     */
    Admin,

    /**
     * Represents the role of an Employee user.
     */
    Employee,

    /**
     * Represents the role of a Manager user.
     */
    Manager
}
