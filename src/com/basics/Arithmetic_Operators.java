package com.basics;

public class Arithmetic_Operators {

	public static void main(String[] args) {
		int x=1;
		int y=2;
//		Add
		System.out.println("x+y="+x+y);
//		sub
		System.out.println("x-y="+(x-y));
//		mul
		System.out.println("x*y="+(x*y));
//		div
		System.out.println("x/y="+ x/y);
//		incre
		System.out.println("x++ ="+ x++);
//		decre
		System.out.println("x-- ="+ x--);
	}
}
