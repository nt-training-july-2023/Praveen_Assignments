package com.basics;

public class Bitwise_and_bitshifting_operators {
	public static void main(String[] args) {
		int a=2;
		a= a << 1;
		System.out.println("Left shif operator a<< "+ a);
		
		int b=8;
		b = b>>1;
		System.out.println("Signed rightshif operator b>> "+ b);
		
		 int c=8;
		 c= c >>> 1;
		System.out.println("Unsigned right shif operator c >>> "+ c);
		
	}
	

}
